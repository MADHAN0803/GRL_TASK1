package calculate_speed;

public class Dataset {

	private double dsDistance;
	private double dsTime;
	private double dsSpeed;


	public double getDsDistance() {
		return dsDistance;
	}
	public void setDsDistance(double dsDistance) {
		this.dsDistance = dsDistance;
	}
	public double getDsTime() {
		return dsTime;
	}
	public void setDsTime(double dsTime) {
		this.dsTime = dsTime;
	}
	public double getDsSpeed() {
		return dsSpeed;
	}
	public void setDsSpeed(double dsDpeed) {
		this.dsSpeed = dsDpeed;
	}

}
